package sample;

public class BRLBXX {
    private String GHBH;
    private String BRMC;
    private String GHRQ;
    private String HZLB;

    public BRLBXX(String GHBH, String BRMC, String GHRQ, String HZLB) {
        this.GHBH = GHBH;
        this.BRMC = BRMC;
        this.GHRQ = GHRQ;
        this.HZLB = HZLB;
    }

    public String getGHBH() {
        return GHBH;
    }

    public String getBRMC() {
        return BRMC;
    }

    public String getGHRQ() {
        return GHRQ;
    }

    public String getHZLB() {
        return HZLB;
    }

    public void setGHBH(String GHBH) {
        this.GHBH = GHBH;
    }

    public void setBRMC(String BRMC) {
        this.BRMC = BRMC;
    }

    public void setGHRQ(String GHRQ) {
        this.GHRQ = GHRQ;
    }

    public void setHZLB(String HZLB) {
        this.HZLB = HZLB;
    }
}
